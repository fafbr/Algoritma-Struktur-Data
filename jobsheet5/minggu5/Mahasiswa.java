package minggu5;
public class Mahasiswa {
    String nama;
    int nim;
    int thnMasuk;
    int nilaiUTS;
    int nilaiUAS;

    public Mahasiswa(String nama, int nim, int thnMsk, int nilaiUts, int nilaiUas) {
        this.nama = nama;
        this.nim = nim;
        this.thnMasuk = thnMsk;
        this.nilaiUTS = nilaiUts;
        this.nilaiUAS = nilaiUas;
    }
}
